#include <iostream>
using namespace std;

int main()
{
    float score, totalItems, percentage;

    cout << "Enter your score:";
    cin >> score;

    cout << "Enter total items:";
    cin >> totalItems;

    percentage = score / totalItems * 100;

    if (percentage > 74.5)
    {
        cout << "Youve passed the quiz (" << percentage << "%)!";
    }
    else
    {
        cout << "Youve failed the quiz (" << percentage << "%)!";
    }

    return 0;
}
